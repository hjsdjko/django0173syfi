      class="add-update-preview"
      ref="ruleForm"
      :model="ruleForm"
      :rules="rules"
      label-width="120px"
    >
          <el-form-item :style='{"width":"48%","padding":"10px","margin":"0 0 10px","background":"none","display":"inline-block"}' label="食谱名称" prop="shipumingcheng">
            <el-input v-model="ruleForm.shipumingcheng" 
                placeholder="食谱名称" clearable :disabled=" false  ||ro.shipumingcheng"></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"48%","padding":"10px","margin":"0 0 10px","background":"none","display":"inline-block"}' label="食谱封面" v-if="type!='cross' || (type=='cross' && !ro.shipufengmian)" prop="shipufengmian">
            <file-upload
            tip="点击上传食谱封面"
            action="file/upload"
            :limit="3"
            :multiple="true"
            :fileUrls="ruleForm.shipufengmian?ruleForm.shipufengmian:''"
            @change="shipufengmianUploadChange"
            ></file-upload>
          </el-form-item>
            <el-form-item :style='{"width":"48%","padding":"10px","margin":"0 0 10px","background":"none","display":"inline-block"}' class="upload" v-else label="食谱封面" prop="shipufengmian">
                <img v-if="ruleForm.shipufengmian.substring(0,4)=='http'" class="upload-img" style="margin-right:20px;" v-bind:key="index" :src="ruleForm.shipufengmian.split(',')[0]" width="100" height="100">
                <img v-else class="upload-img" style="margin-right:20px;" v-bind:key="index" v-for="(item,index) in ruleForm.shipufengmian.split(',')" :src="baseUrl+item" width="100" height="100">
            </el-form-item>
          <el-form-item :style='{"width":"48%","padding":"10px","margin":"0 0 10px","background":"none","display":"inline-block"}'  label="慢性病" prop="manxingbing">
            <el-select v-model="ruleForm.manxingbing" placeholder="请选择慢性病" :disabled=" false  ||ro.manxingbing" >
              <el-option
                  v-for="(item,index) in manxingbingOptions"
                  :key="index"
                  :label="item"
                  :value="item">
              </el-option>
            </el-select>
          </el-form-item>
          <el-form-item :style='{"width":"48%","padding":"10px","margin":"0 0 10px","background":"none","display":"inline-block"}' label="食谱功效" prop="shipugongxiao">
            <el-input
              type="textarea"
              :rows="8"
              placeholder="食谱功效"
              v-model="ruleForm.shipugongxiao">
            </el-input>
          </el-form-item>
          <el-form-item :style='{"width":"48%","padding":"10px","margin":"0 0 10px","background":"none","display":"inline-block"}' label="营养成分" prop="yingyangchengfen">
            <el-input
              type="textarea"
              :rows="8"
              placeholder="营养成分"
              v-model="ruleForm.yingyangchengfen">
            </el-input>
          </el-form-item>
