<script>
  export default {
    data() {
	  let self = this
      return {
        id: '',
        baseUrl: '',
        ro:{
				jiaoliubianhao : false,
				renyuanbianhao : false,
				renyuanxingming : false,
				jiaoliuzhe : false,
				xingming : false,
				neirong : false,
				fasongshijian : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          jiaoliubianhao: this.getUUID(),
          renyuanbianhao: '',
          renyuanxingming: '',
          jiaoliuzhe: '',
          xingming: '',
          neirong: '',
          fasongshijian: '',
        },


        rules: {
          jiaoliubianhao: [
          ],
          renyuanbianhao: [
          ],
          renyuanxingming: [
          ],
          jiaoliuzhe: [
          ],
          xingming: [
          ],
          neirong: [
            { required: true, message: '内容不能为空', trigger: 'blur' },
          ],
          fasongshijian: [
          ],
        },
		centerType: false,
      };
    },
    computed: {



    },
    components: {
    },
    created() {
		if(this.$route.query.centerType){
			this.centerType = true
		}
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.fasongshijian = this.getCurDateTime()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='jiaoliubianhao'){
              this.ruleForm.jiaoliubianhao = obj[o];
              this.ro.jiaoliubianhao = true;
              continue;
            }
            if(o=='renyuanbianhao'){
              this.ruleForm.renyuanbianhao = obj[o];
              this.ro.renyuanbianhao = true;
              continue;
            }
            if(o=='renyuanxingming'){
              this.ruleForm.renyuanxingming = obj[o];
              this.ro.renyuanxingming = true;
              continue;
            }
            if(o=='jiaoliuzhe'){
              this.ruleForm.jiaoliuzhe = obj[o];
              this.ro.jiaoliuzhe = true;
              continue;
            }
            if(o=='xingming'){
              this.ruleForm.xingming = obj[o];
              this.ro.xingming = true;
              continue;
            }
            if(o=='neirong'){
              this.ruleForm.neirong = obj[o];
              this.ro.neirong = true;
              continue;
            }
            if(o=='fasongshijian'){
              this.ruleForm.fasongshijian = obj[o];
              this.ro.fasongshijian = true;
              continue;
            }
          }
        }else if(type=='edit'){
			this.info()
		}
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
            if((json.renyuanbianhao!=''&&json.renyuanbianhao) || json.renyuanbianhao==0){
                this.ruleForm.jiaoliuzhe = json.renyuanbianhao
            }
            if((json.renyuanxingming!=''&&json.renyuanxingming) || json.renyuanxingming==0){
                this.ruleForm.xingming = json.renyuanxingming
            }
          }
        });

		if (localStorage.getItem('raffleType') && localStorage.getItem('raffleType') != null) {
			localStorage.removeItem('raffleType')
			setTimeout(() => {
				this.onSubmit()
			}, 300)
		}
      },

    // 多级联动参数
      // 多级联动参数
      info() {
        this.$http.get(`xinxijiaoliu/detail/${this.$route.query.id}`, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

			if(this.ruleForm.jiaoliubianhao){
				this.ruleForm.jiaoliubianhao = String(this.ruleForm.jiaoliubianhao)
			}
			//更新跨表属性
			var crossuserid;
			var crossrefid;
			var crossoptnum;
			this.$refs["ruleForm"].validate(valid => {
				if(valid) {
					if(this.type=='cross'){
						var statusColumnName = localStorage.getItem('statusColumnName');
						var statusColumnValue = localStorage.getItem('statusColumnValue');
						if(statusColumnName && statusColumnName!='') {
							var obj = JSON.parse(localStorage.getItem('crossObj'));
							if(!statusColumnName.startsWith("[")) {
								for (var o in obj){
									if(o==statusColumnName){
										obj[o] = statusColumnValue;
									}
								}
								var table = localStorage.getItem('crossTable');
								this.$http.post(table+'/update', obj).then(res => {});
							} else {
								crossuserid=Number(localStorage.getItem('frontUserid'));
								crossrefid=obj['id'];
								crossoptnum=localStorage.getItem('statusColumnName');
								crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
							}
						}
					}
					if(crossrefid && crossuserid) {
						this.ruleForm.crossuserid=crossuserid;
						this.ruleForm.crossrefid=crossrefid;
						var params = {
							page: 1,
							limit: 10,
							crossuserid:crossuserid,
							crossrefid:crossrefid,
						}
						this.$http.get('xinxijiaoliu/list', {
							params: params
						}).then(res => {
							if(res.data.data.total>=crossoptnum) {
								this.$message({
									message: localStorage.getItem('tips'),
									type: 'error',
									duration: 1500,
								});
								return false;
							} else {
								// 跨表计算


								this.$http.post(`xinxijiaoliu/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
									if (res.data.code == 0) {
										this.$message({
											message: '操作成功',
											type: 'success',
											duration: 1500,
											onClose: () => {
												this.$router.go(-1);
											}
										});
									} else {
										this.$message({
											message: res.data.msg,
											type: 'error',
											duration: 1500
										});
									}
								});
							}
						});
					} else {


						this.$http.post(`xinxijiaoliu/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
							if (res.data.code == 0) {
								this.$message({
									message: '操作成功',
									type: 'success',
									duration: 1500,
									onClose: () => {
										this.$router.go(-1);
									}
								});
							} else {
								this.$message({
									message: res.data.msg,
									type: 'error',
									duration: 1500
								});
							}
						});
					}
				}
			});
		},
		// 获取uuid
		getUUID () {
			return new Date().getTime();
		},
		// 返回
		back() {
			this.$router.go(-1);
		},
    }
  };
</script>

