<script>
  export default {
    data() {
	  let self = this
      return {
        id: '',
        baseUrl: '',
        ro:{
				dingdanbianhao : false,
				renyuanbianhao : false,
				renyuanxingming : false,
				lianxidianhua : false,
				fuwuleixing : false,
				zhaopian : false,
				fuwufei : false,
				jifeibiaozhun : false,
				laorenzhanghao : false,
				laorenxingming : false,
				shoujihaoma : false,
				dizhi : false,
				fuwuxuqiu : false,
				xiadanshijian : false,
				sfsh : false,
				shhf : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          dingdanbianhao: this.getUUID(),
          renyuanbianhao: '',
          renyuanxingming: '',
          lianxidianhua: '',
          fuwuleixing: '',
          zhaopian: '',
          fuwufei: '',
          jifeibiaozhun: '',
          laorenzhanghao: '',
          laorenxingming: '',
          shoujihaoma: '',
          dizhi: '',
          fuwuxuqiu: '',
          xiadanshijian: '',
        },


        rules: {
          dingdanbianhao: [
          ],
          renyuanbianhao: [
          ],
          renyuanxingming: [
          ],
          lianxidianhua: [
          ],
          fuwuleixing: [
          ],
          zhaopian: [
          ],
          fuwufei: [
          ],
          jifeibiaozhun: [
          ],
          laorenzhanghao: [
          ],
          laorenxingming: [
          ],
          shoujihaoma: [
          ],
          dizhi: [
            { required: true, message: '地址不能为空', trigger: 'blur' },
          ],
          fuwuxuqiu: [
            { required: true, message: '服务需求不能为空', trigger: 'blur' },
          ],
          xiadanshijian: [
          ],
          sfsh: [
          ],
          shhf: [
          ],
        },
		centerType: false,
      };
    },
    computed: {



    },
    components: {
    },
    created() {
		if(this.$route.query.centerType){
			this.centerType = true
		}
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.xiadanshijian = this.getCurDateTime()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='dingdanbianhao'){
              this.ruleForm.dingdanbianhao = obj[o];
              this.ro.dingdanbianhao = true;
              continue;
            }
            if(o=='renyuanbianhao'){
              this.ruleForm.renyuanbianhao = obj[o];
              this.ro.renyuanbianhao = true;
              continue;
            }
            if(o=='renyuanxingming'){
              this.ruleForm.renyuanxingming = obj[o];
              this.ro.renyuanxingming = true;
              continue;
            }
            if(o=='lianxidianhua'){
              this.ruleForm.lianxidianhua = obj[o];
              this.ro.lianxidianhua = true;
              continue;
            }
            if(o=='fuwuleixing'){
              this.ruleForm.fuwuleixing = obj[o];
              this.ro.fuwuleixing = true;
              continue;
            }
            if(o=='zhaopian'){
              this.ruleForm.zhaopian = obj[o].split(",")[0];
              this.ro.zhaopian = true;
              continue;
            }
            if(o=='fuwufei'){
              this.ruleForm.fuwufei = obj[o];
              this.ro.fuwufei = true;
              continue;
            }
            if(o=='jifeibiaozhun'){
              this.ruleForm.jifeibiaozhun = obj[o];
              this.ro.jifeibiaozhun = true;
              continue;
            }
            if(o=='laorenzhanghao'){
              this.ruleForm.laorenzhanghao = obj[o];
              this.ro.laorenzhanghao = true;
              continue;
            }
            if(o=='laorenxingming'){
              this.ruleForm.laorenxingming = obj[o];
              this.ro.laorenxingming = true;
              continue;
            }
            if(o=='shoujihaoma'){
              this.ruleForm.shoujihaoma = obj[o];
              this.ro.shoujihaoma = true;
              continue;
            }
            if(o=='dizhi'){
              this.ruleForm.dizhi = obj[o];
              this.ro.dizhi = true;
              continue;
            }
            if(o=='fuwuxuqiu'){
              this.ruleForm.fuwuxuqiu = obj[o];
              this.ro.fuwuxuqiu = true;
              continue;
            }
            if(o=='xiadanshijian'){
              this.ruleForm.xiadanshijian = obj[o];
              this.ro.xiadanshijian = true;
              continue;
            }
          }
        }else if(type=='edit'){
			this.info()
		}
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
            if((json.laorenzhanghao!=''&&json.laorenzhanghao) || json.laorenzhanghao==0){
                this.ruleForm.laorenzhanghao = json.laorenzhanghao
            }
            if((json.laorenxingming!=''&&json.laorenxingming) || json.laorenxingming==0){
                this.ruleForm.laorenxingming = json.laorenxingming
            }
            if((json.shoujihaoma!=''&&json.shoujihaoma) || json.shoujihaoma==0){
                this.ruleForm.shoujihaoma = json.shoujihaoma
            }
          }
        });

		if (localStorage.getItem('raffleType') && localStorage.getItem('raffleType') != null) {
			localStorage.removeItem('raffleType')
			setTimeout(() => {
				this.onSubmit()
			}, 300)
		}
      },

    // 多级联动参数
      // 多级联动参数
      info() {
        this.$http.get(`fuwudingdan/detail/${this.$route.query.id}`, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

			if(this.ruleForm.dingdanbianhao){
				this.ruleForm.dingdanbianhao = String(this.ruleForm.dingdanbianhao)
			}
			//更新跨表属性
			var crossuserid;
			var crossrefid;
			var crossoptnum;
			this.$refs["ruleForm"].validate(valid => {
				if(valid) {
					if(this.type=='cross'){
						var statusColumnName = localStorage.getItem('statusColumnName');
						var statusColumnValue = localStorage.getItem('statusColumnValue');
						if(statusColumnName && statusColumnName!='') {
							var obj = JSON.parse(localStorage.getItem('crossObj'));
							if(!statusColumnName.startsWith("[")) {
								for (var o in obj){
									if(o==statusColumnName){
										obj[o] = statusColumnValue;
									}
								}
								var table = localStorage.getItem('crossTable');
								this.$http.post(table+'/update', obj).then(res => {});
							} else {
								crossuserid=Number(localStorage.getItem('frontUserid'));
								crossrefid=obj['id'];
								crossoptnum=localStorage.getItem('statusColumnName');
								crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
							}
						}
					}
					if(crossrefid && crossuserid) {
						this.ruleForm.crossuserid=crossuserid;
						this.ruleForm.crossrefid=crossrefid;
						var params = {
							page: 1,
							limit: 10,
							crossuserid:crossuserid,
							crossrefid:crossrefid,
						}
						this.$http.get('fuwudingdan/list', {
							params: params
						}).then(res => {
							if(res.data.data.total>=crossoptnum) {
								this.$message({
									message: localStorage.getItem('tips'),
									type: 'error',
									duration: 1500,
								});
								return false;
							} else {
								// 跨表计算


								this.$http.post(`fuwudingdan/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
									if (res.data.code == 0) {
										this.$message({
											message: '操作成功',
											type: 'success',
											duration: 1500,
											onClose: () => {
												this.$router.go(-1);
											}
										});
									} else {
										this.$message({
											message: res.data.msg,
											type: 'error',
											duration: 1500
										});
									}
								});
							}
						});
					} else {


						this.$http.post(`fuwudingdan/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
							if (res.data.code == 0) {
								this.$message({
									message: '操作成功',
									type: 'success',
									duration: 1500,
									onClose: () => {
										this.$router.go(-1);
									}
								});
							} else {
								this.$message({
									message: res.data.msg,
									type: 'error',
									duration: 1500
								});
							}
						});
					}
				}
			});
		},
		// 获取uuid
		getUUID () {
			return new Date().getTime();
		},
		// 返回
		back() {
			this.$router.go(-1);
		},
      zhaopianUploadChange(fileUrls) {
          this.ruleForm.zhaopian = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");;
      },
    }
  };
</script>

